
package Entidades;

public class Mascota {
public String nombre;
public String apodo;
public String tipo;
public int edad;
public String color;
public boolean cola;
public String raza;

    public Mascota() {
    }
public Mascota (String nombre, String apodo, int edad){
    this.nombre=nombre;
    this.apodo=apodo;
    this.edad=edad;
}
    public Mascota(String nombre, String apodo, String tipo, int edad, String color, boolean cola, String raza) {
        this.nombre = nombre;
        this.apodo = apodo;
        this.tipo = tipo;
        this.edad = edad;
        this.color = color;
        this.cola = cola;
        this.raza = raza;
    }










}
